# 1. Cálculo Diário do Índice de Angström e Classificação de Perigo
fire_risk_daily = climate_data[climate_data['Ano'] >= 1917].dropna(subset=['TMAX', 'URMED']).copy()
fire_risk_daily['Angstrom_Index'] = (fire_risk_daily['URMED'] / 20) + ((fire_risk_daily['TMAX'] - 27) / 10)

# Classificação do Risco Florestal
def classe_risco(angstrom):
    if angstrom > 4.0: return "Nulo/Baixo"
    elif angstrom > 2.5: return "Moderado"
    elif angstrom > 2.0: return "Alto"
    else: return "Muito Alto a Extremo"

fire_risk_daily['Classe_Risco'] = fire_risk_daily['Angstrom_Index'].apply(classe_risco)
risk_categories = ["Nulo/Baixo", "Moderado", "Alto", "Muito Alto a Extremo"]
fire_risk_daily['Classe_Risco'] = pd.Categorical(fire_risk_daily['Classe_Risco'], categories=risk_categories, ordered=True)

# 2. Agrupamento Anual para Tendência
fire_risk_annual = fire_risk_daily.groupby('Ano').agg(
    Mean_Angstrom=('Angstrom_Index', 'mean'),
    Days_Critical_Risk=('Angstrom_Index', lambda x: (x <= 2.5).sum())
).reset_index()

