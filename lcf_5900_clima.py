def trend_summary(df, year_col, var_col):
    """Executa o teste de Mann-Kendall e calcula o Sen's Slope omitindo NaNs."""
    clean_df = df[[year_col, var_col]].dropna()
    res = mk.original_test(clean_df[var_col])

    return pd.DataFrame({
        'Sen_Slope': [res.slope],
        'MK_Tau': [res.tau],
        'p_value': [res.p],
        'Significant': ['Yes' if res.p < 0.05 else 'No']
    })

def moving_avg(series, n=10):
    """Calcula a média móvel centralizada."""
    return series.rolling(window=n, center=True).mean()

